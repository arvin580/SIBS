import string
def getChr(chr='chrX'):
    inFile=open('ucsc.hg19.fasta.fa')
    flag=0
    while True :
        line=inFile.readline().strip()
        if flag==1 :
            return line
        if line.find(chr)==1 :
            flag=1
        if not line :
            break
    inFile.close()



def getSeq(chr,strand,listStart,listEnd) :
    chrSeq=getChr(chr)
    exon=[]
    trans=string.maketrans('ATCGatcg','TAGCtagc')
    for i in range(len(listStart)) :
        seq=chrSeq[listStart[i]:listEnd[i]]
        if strand=='+' :
            exon.append(seq)
        else :
            seq=seq[::-1]
            seq=seq.translate(trans)
            exon.append(seq)
    return exon



